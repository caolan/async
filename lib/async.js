    async.sortBy = function (arr, iterator, callback) {
        async.map(arr, function (x, callback) {
            iterator(x, function (err, criteria) {
                if (err) {
                    callback(err);
                }
                else {
                    callback(null, {value: x, criteria: criteria});
                }
            });
        }, function (err, results) {
            if (err) {
                return callback(err);
            }
            else {
                var fn = function (left, right) {
                    var a = left.criteria, b = right.criteria;
                    return a < b ? -1 : a > b ? 1 : 0;
                };
                callback(null, _map(results.sort(fn), function (x) {
                    return x.value;
                }));
            }
        });
    };

    async.auto = function (tasks, callback) {
        callback = callback || function () {};
        var keys = _keys(tasks);
        if (!keys.length) {
            return callback(null);
        }

        var errors = {};
        var results = {};
        var running = {};
        var completed = [];
        var listeners = [];
        var needsFinalizing = [];
        var finalized = {};
        var addListener = function (fn) {
            listeners.push(fn);
        };
        var removeListener = function (fn) {
            for (var i = 0; i < listeners.length; i++) {
                if (listeners[i] === fn) {
                    listeners.splice(i, 1);
                    return;
                }
            }
        };
        var taskComplete = function () {
            _forEach(listeners.slice(0), function (fn) {
                fn();
            });
        };

        var taskError = function() {
            var rollback = {};
            var awaiting = {};

            _forEach(completed, function(t) {
                if(finalized.hasOwnProperty(t.key) || ! t.undoes || _indexOf(needsFinalizing, t.key) === -1) {
                    return;
                }
                rollback[t.key] = async.task()
                    .run(function(callback, results) {
                        t.undoes(errors, callback, results);
                    });
                // unfinalize any task that was finalized by this task
                _forEach(_keys(finalized), function(key) {
                    if(finalized[key] == t) {
                        delete finalized[key];
                    }
                });
                // if this task awaited any tasks, their undoing should await this tasks undoing
                _forEach(t.awaits, function(key) {
                    if(finalized.hasOwnProperty(key) || _indexOf(needsFinalizing, key) == -1) {
                        return;
                    }
                    (awaiting.hasOwnProperty(key) ? awaiting[key] : (awaiting[key] = [])).push(t.key);
                });
            });

            _forEach(_keys(awaiting), function(k) {
                rollback[k].await(awaiting[k]);
            });

            async.auto(rollback, function(err, results) {
                callback(errors);
            });
        };

        var runTask = function() {
            
        };

        addListener(function () {
            if (_keys(results).length === keys.length) {
                callback(null, results);
                callback = function () {};
            }
        });

        _forEach(keys, function (k) {
            var task;
            if(tasks[k] instanceof _task) {
                task = tasks[k];
            }
            else {
                tasks[k] = task = (tasks[k] instanceof Function) ? async.task().run(tasks[k]) : async.task().run(tasks[k].pop()).await(tasks[k]);
            }
            task.key = k;
            _forEach(task.finalizes, function(fk) {
                if(_indexOf(needsFinalizing, fk) === -1) {
                    needsFinalizing.push(fk);
                }
            });

            var taskCallback = function (err) {
                if (err) {
                    errors[k] = err;
                }
                else {
                    completed.unshift(tasks[k]);
                    _forEach(task.finalizes, function(t) {
                        tasks[t].finalized = true;
                        finalized[t] = finalized[t] || task;
                    });
                    var args = Array.prototype.slice.call(arguments, 1);
                    if (args.length <= 1) {
                        args = args[0];
                    }
                    results[k] = args;
                }
                
                delete running[k];
                async.nextTick(function() {
                    if(_keys(errors).length) {
                        if(_keys(running).length === 0) {
                            taskError();
                            taskError = function() {};
                        }
                    }
                    else {
                        taskComplete();
                    }
                    
                });
            };

            var ready = function () {
                return _reduce(task.awaits, function (a, x) {
                    return (a && results.hasOwnProperty(x));
                }, true);
            };
            var listener = function () {
                if (ready()) {
                    running[k] = task;
                    removeListener(listener);
                    if(task.handler) {
                        try {
                            task.does(taskCallback, results);
                        }
                        catch(err) {
                            task.handler(err, taskCallback);
                        }
                    }
                    else task.does(taskCallback, results);
                }
            };
            addListener(listener);
            async.nextTick(taskComplete);
        });
    };

    async.waterfall = function (tasks, callback) {
        callback = callback || function () {};
        if (!tasks.length) {
            return callback();
        }
        var wrapIterator = function (iterator) {
            return function (err) {
                if (err) {
                    callback(err);
                    callback = function () {};
                }
                else {
                    var args = Array.prototype.slice.call(arguments, 1);
                    var next = iterator.next();
                    if (next) {
                        args.push(wrapIterator(next));
                    }
                    else {
                        args.push(callback);
                    }
                    async.nextTick(function () {
                        iterator.apply(null, args);
                    });
                }
            };
        };
        wrapIterator(async.iterator(tasks))();
    };

    async.parallel = function (tasks, callback) {
        callback = callback || function () {};
        if (tasks.constructor === Array) {
            async.map(tasks, function (fn, callback) {
                if (fn) {
                    fn(function (err) {
                        var args = Array.prototype.slice.call(arguments, 1);
                        if (args.length <= 1) {
                            args = args[0];
                        }
                        callback.call(null, err, args);
                    });
                }
            }, callback);
        }
        else {
            var results = {};
            async.forEach(_keys(tasks), function (k, callback) {
                tasks[k](function (err) {
                    var args = Array.prototype.slice.call(arguments, 1);
                    if (args.length <= 1) {
                        args = args[0];
                    }
                    results[k] = args;
                    callback(err);
                });
            }, function (err) {
                callback(err, results);
            });
        }
    };

